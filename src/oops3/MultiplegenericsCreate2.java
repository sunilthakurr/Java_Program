package oops3;

public class MultiplegenericsCreate2<T, V> {
    T first;
    V second;

    MultiplegenericsCreate2(T first, V second) {
        this.first = first;
        this.second = second;
    }

    public T getFirst() {
        return first;
    }

    public void setFirst(T first) {
        this.first = first;
    }

    public V getSecond() {
        return second;
    }

    public void setSecond(V second) {
        this.second = second;
    }
}
